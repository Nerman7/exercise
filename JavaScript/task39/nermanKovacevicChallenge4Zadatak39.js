/*Napisati program koji ispisuje ime mjeseca na osnovu cijelog broja. 
(1 - Januar, 2 - Februar, ... , 12 - Decembar ). 
Cijeli broj predstaviti varijablom. Na osnovu cijelog broja ispisati sljedecu poruku.

    Primjer poruke: ‘Broju 7 odgovara mjesec: Juli.’*/

let mjesec = 6;

switch (mjesec) {
  case 1:
    console.log(`Broju 1 odgovara mjesec: Januar.`);
    break;
  case 2:
    console.log(`Broju 2 odgovara mjesec: Februar.`);
    break;
  case 3:
    console.log(`Broju 3 odgovara mjesec: Mart.`);
    break;
  case 4:
    console.log(`Broju 4 odgovara mjesec: April.`);
    break;
  case 5:
    console.log(`Broju 5 odgovara mjesec: Maj.`);
    break;
  case 6:
    console.log(`Broju 6 odgovara mjesec: Juni.`);
    break;
  case 7:
    console.log(`Broju 7 odgovara mjesec: Juli.`);
    break;
  case 8:
    console.log(`Broju 8 odgovara mjesec: August.`);
    break;
  case 9:
    console.log(`Broju 9 odgovara mjesec: Septembar.`);
    break;
  case 10:
    console.log(`Broju 10 odgovara mjesec: Oktobar.`);
    break;
  case 11:
    console.log(`Broju 11 odgovara mjesec: Novembar.`);
    break;
  case 12:
    console.log(`Broju 12 odgovara mjesec: Decembar.`);
    break;
}
