/*5. Napisati program koji ispisuje brojeve od 1 do 10 slovima.*/

for (let i = 1; i <= 10; i++) {
  switch (i) {
    case 1:
      console.log("jedan");
      break;
    case 2:
      console.log("dva");
      break;
    case 3:
      console.log("tri");
      break;
    case 4:
      console.log("cetiri");
      break;
    case 5:
      console.log("pet");
      break;
    case 6:
      console.log("sest");
      break;
    case 7:
      console.log("sedam");
      break;
    case 8:
      console.log("osam");
      break;
    case 9:
      console.log("devet");
      break;
    case 10:
      console.log("deset");
      break;
  }
}
